package ud_2;

import java.util.Scanner;

public class ModificarVariableX {
	
	
    public static void main(String[] args) {
    	
        int x = 10;
        System.out.println("Valor inicial de x: " + x);

        x -= 5;  
        System.out.println("Después de restar 5: " + x);

        x += 1;  
        System.out.println("Después de sumar 1: " + x);

        x *= 2;  
        System.out.println("Después de multiplicar por 2: " + x);

        x /= 2;  
        System.out.println("Después de dividir por 2: " + x);

        x -= 10;  
        System.out.println("Después de restar 10: " + x);

        x +=  5;  
        System.out.println("Después de sumar 5: " + x);
        
        
       
        
    }
}

